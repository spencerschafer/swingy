    private void CreateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CreateButtonActionPerformed
        // TODO add your handling code here:
        if (selectClassComboBox.getSelectedItem() == "Select Class") {
            this.firePropertyChange("Default", null, evt);
        } else {
            if (name == null) {
                //
                this.firePropertyChange("nullName", null, evt);
            } else if (type == null) {
                this.firePropertyChange("nullType", null, evt);
            } else {
                hero = new Hero(name, type);
                this.firePropertyChange("Load", null, evt);
            }
        }
    }//GEN-LAST:event_CreateButtonActionPerformed

